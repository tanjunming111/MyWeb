// ===== 数据读写接口 =====

function loadEssayData() {
    return defaultEssayData;
}

function saveEssayData(data) {
    defaultEssayData = data;
}

function getEssayById(id) {
    return defaultEssayData.find(function(e) { return e.id === id; });
}

function addEssay(id, title, date, summary, content) {
    if (defaultEssayData.find(function(e) { return e.id === id; })) return false;
    defaultEssayData.push({ id: id, title: title, date: date, summary: summary, content: content });
    defaultEssayData.sort(function(a, b) { return a.id - b.id; });
    return true;
}

function updateEssay(id, title, date, summary, content) {
    var essay = defaultEssayData.find(function(e) { return e.id === id; });
    if (essay) {
        essay.title = title;
        essay.date = date;
        essay.summary = summary;
        essay.content = content;
        return true;
    }
    return false;
}

function deleteEssay(id) {
    var newData = defaultEssayData.filter(function(e) { return e.id !== id; });
    if (newData.length === defaultEssayData.length) return false;
    defaultEssayData = newData;
    return true;
}